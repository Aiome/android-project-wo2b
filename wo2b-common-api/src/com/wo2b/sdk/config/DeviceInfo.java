package com.wo2b.sdk.config;

/**
 * 设备信息
 * 
 * @author 笨鸟不乖
 * 
 */
public class DeviceInfo
{

	/** 设备类型 */
	private String type;

	/** 设备名称 */
	private String name;

	public String getType()
	{
		return type;
	}

	public void setType(String type)
	{
		this.type = type;
	}

	public String getName()
	{
		return name;
	}

	public void setName(String name)
	{
		this.name = name;
	}

}
